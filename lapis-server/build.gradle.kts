// The Kotlin JVM Gradle plugin auto-registers `compileKumlGeneratedKotlin` for the `kumlGenerated`
// source set above. It must run after `test` (which writes the sources it compiles) and gates
// `check` — a syntactically/semantically invalid generated file fails `./gradlew clean check`.
//
// Declaring the directory as a `test` task *output* (not just a plain `dependsOn`) matters here:
// without it, a `./gradlew clean check` run (this repo's mandatory pre-build step, see CLAUDE.md)
// has been observed to make `compileKumlGeneratedKotlin` report NO-SOURCE and silently skip
// compilation entirely — Gradle's incremental/file-system-watching layer does not reliably pick
// up files written as an undeclared side effect of `test` immediately after the directory was
// freshly re-created by `clean`. Declaring the output wires it into Gradle's normal
// producer/consumer up-to-date tracking, which fixes this reliably.
tasks.test {
    useJUnitPlatform()
    outputs.dir(layout.buildDirectory.dir("generated/kuml"))
}

tasks.named("compileKumlGeneratedKotlin") {
    dependsOn(tasks.test)
}

// ktlint auto-registers a check task per source set, including the new `kumlGenerated` one. Since
// its input directory is now a declared `test` output (see above), Gradle's task-validation
// requires an explicit ordering dependency here too, or `./gradlew clean check` fails with an
// "implicit dependency" configuration error.
tasks.named("runKtlintCheckOverKumlGeneratedSourceSet") {
    dependsOn(tasks.test)
}

tasks.check {
    dependsOn("compileKumlGeneratedKotlin")
}
