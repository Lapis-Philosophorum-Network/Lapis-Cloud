// ADR-0016 MDA codegen verification pipeline — real-codegen part (a): "does the generated Kotlin
// actually compile?". Deliberately a separate, isolated Gradle source set (not `main`/`test`) so
// this never touches the production compile classpath:
//   - `CodegenParitySeedTest` (in `src/test/kotlin`) runs `UmlToExposedViaErmScriptTransformer`
//     for every domain script and writes the generated `Table`/enum sources into
//     `build/generated/kuml/<package-path>/*.kt` as a *side effect* of running `test`.
//   - This `kumlGenerated` source set then compiles exactly those written files against a small,
//     Exposed-only classpath. A syntactically/semantically invalid generated file fails the build
//     right here instead of silently rotting.
// No custom `doFirst`/`doLast` Gradle task is used to do the file-writing (see CLAUDE.md's
// kUML-Repo-Konventionen "Custom-Task-Config-Cache-Falle" — Gradle 9's configuration cache has
// repeatedly broken on custom tasks that touch external file directories in this codebase); the
// file-writing lives in ordinary test code instead, exactly like
// `VaultExamplesRenderTest.kt`'s `afterSpec` pattern in the kUML repo.
